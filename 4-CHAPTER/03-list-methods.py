# for oredering items in a list we used list.sort()
sort = [1,3,2,7,9,8,5,4,10]
sort.sort()
print(sort)
print('***************')

# reverse the list using list.reverse()
reverse = [1,2,3,4,5]
reverse.reverse()
print(reverse)
print('***************')

# Addition of new value at the end of the list using list.append(value)
append = [1,2,3,4]
append.append(5)
print(append)
print('***************')

# insert new value to perticular index in a list using list.insert(index, value)
insert = [1,2,3,4]
insert.insert(2, 'Nilesh')
print(insert)
print('***************')

# for removing any element at given index we used list.pop(index)
pop = [1,2,3,4,5]
pop.pop(2)
print(pop)
print('***************')

# removing value from list then used list.remove(value)
remove = [11,12,13,14,15]
remove.remove(13)
print(remove)
print('***************')
